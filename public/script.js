function showSuggestions() {
    const input = document.getElementById('pokemonInput');
    const inputValue = input.value.toLowerCase();
    const suggestions = document.getElementById('suggestions');
    suggestions.innerHTML = '';
  
    // Filter fruits based on input value
    const filteredPokemons = pokemons.filter(fruit => fruit.toLowerCase().startsWith(inputValue));
    
    // Display suggestions
    filteredPokemons.forEach(pokemon => {
      const suggestion = document.createElement('a');
      suggestion.href = `/${pokemon}`;
      suggestion.textContent = pokemon;
      suggestions.appendChild(suggestion);
    });
  
    // Show suggestion box
    if (inputValue.length > 0 && filteredPokemons.length > 0) {
      suggestions.style.display = 'block';
    } else {
      suggestions.style.display = 'none';
    }
}

document.addEventListener('click', function(event) {
    const suggestions = document.getElementById('suggestions');
    if (!event.target.matches('#pokemonInput') && !event.target.matches('.suggestion-box a')) {
      suggestions.style.display = 'none';
    }
});

function setProgressBarWidth(statValue, statName) {
  const progressBar = document.getElementById('statProgressBar' + statName);
  if (progressBar) {
      progressBar.style.width = statValue + '%';
  }
}
